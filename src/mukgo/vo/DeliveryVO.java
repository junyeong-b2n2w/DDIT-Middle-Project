package mukgo.vo;

public class DeliveryVO {
	private int deli_num;
	private int deli_ord_num;
	private String deli_man;
	private int deli_eta;
	private String deli_arrival;
	private String deli_status;
	public int getDeli_num() {
		return deli_num;
	}
	public void setDeli_num(int deli_num) {
		this.deli_num = deli_num;
	}
	public int getDeli_ord_num() {
		return deli_ord_num;
	}
	public void setDeli_ord_num(int deli_ord_num) {
		this.deli_ord_num = deli_ord_num;
	}
	public String getDeli_man() {
		return deli_man;
	}
	public void setDeli_man(String deli_man) {
		this.deli_man = deli_man;
	}
	public int getDeli_eta() {
		return deli_eta;
	}
	public void setDeli_eta(int deli_eta) {
		this.deli_eta = deli_eta;
	}
	public String getDeli_arrival() {
		return deli_arrival;
	}
	public void setDeli_arrival(String deli_arrival) {
		this.deli_arrival = deli_arrival;
	}
	public String getDeli_status() {
		return deli_status;
	}
	public void setDeli_status(String deli_status) {
		this.deli_status = deli_status;
	}
}
